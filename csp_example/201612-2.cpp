#include<stdio.h>

int main(){
	
	int t,s,a;
	int s_f,s_t;
	int t_new;
	scanf("%d",&t);
	for(s_f=(t/100)*100;;s_f+=100){
		if(s_f<=3500){
			t_new = s_f;
			if(t_new == t){
				s_t = s_f;
				break;
			}
		}
		a = s_f - 3500;
		if(a<=1500){
			t_new = s_f - a*0.03;
		}else if(a>1500 && a<=4500){
			t_new = s_f - 1500*0.03 - (a-1500)*0.1;
		}else if(a>4500 && a<=9000){
			t_new = s_f - 1500*0.03 - (4500 - 1500)*0.1 - (a-4500)*0.2;
		}else if(a>9000 && a<=35000){
			t_new = s_f - 1500*0.03 - (4500 - 1500)*0.1 - (9000-4500)*0.2 - (a-9000)*0.25;
		}else if(a>35000 && a<=55000){
			t_new = s_f - 1500*0.03 - (4500 - 1500)*0.1 - (9000-4500)*0.2 - (35000-9000)*0.25 - (a-35000)*0.3;
		}else if(a>55000 && a<=80000){
			t_new = s_f - 1500*0.03 - (4500 - 1500)*0.1 - (9000-4500)*0.2 - (35000-9000)*0.25 - (55000-35000)*0.3 - (a-55000)*0.35;
		}else{
			t_new = s_f - 1500*0.03 - (4500 - 1500)*0.1 - (9000-4500)*0.2 - (35000-9000)*0.25 - (55000-35000)*0.3 - (80000-55000)*0.35 - (a-80000)*0.45;
		}
		if(t_new == t){
			s_t = s_f;
			break;
		}
		
	}
	printf("%d\n",s_t);
	
	return 0;
}
